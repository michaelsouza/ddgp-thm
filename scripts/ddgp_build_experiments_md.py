#!/usr/bin/env python3
"""Build a Markdown report for selected DDGP experiments."""

from __future__ import annotations

import argparse
import csv
import json
import os
from pathlib import Path


IMAGE_SUFFIXES = [
    ("full", "Full adjacency matrix"),
    ("fix-union", "Induced matrix on fixing sets"),
    ("dependency-dag", "Dependency DAG by level"),
    ("base-clique-graph", "Base-clique transition graph"),
]


def load_counts(path: Path) -> dict[str, dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as fh:
        return {row["id"]: row for row in csv.DictReader(fh)}


def load_instances(path: Path) -> list[dict]:
    records = []
    with path.open(encoding="utf-8") as fh:
        for line in fh:
            if line.strip():
                records.append(json.loads(line))
    return records


def rel_link(target: Path, output: Path) -> str:
    return os.path.relpath(target, start=output.parent).replace(os.sep, "/")


def predecessor_sets_text(record: dict) -> str:
    parts = []
    for vertex, predecessors in sorted(record["predecessor_sets"].items(), key=lambda item: int(item[0])):
        parts.append(f"$U_{{{vertex}}}=({', '.join(str(u) for u in predecessors)})$")
    return ", ".join(parts)


def image_table(instance_id: str, image_dir: Path, output: Path) -> list[str]:
    lines = [
        "<table>",
        "<tr>",
    ]
    for _, label in IMAGE_SUFFIXES:
        lines.append(f"<th>{label}</th>")
    lines.extend(["</tr>", "<tr>"])

    for suffix, label in IMAGE_SUFFIXES:
        image = image_dir / f"{instance_id}-{suffix}.png"
        href = rel_link(image, output)
        lines.append(f'<td><img src="{href}" alt="{instance_id} - {label}" width="360"></td>')

    lines.extend(["</tr>", "</table>"])
    return lines


def build_report(instances: list[dict], counts_by_id: dict[str, dict[str, str]], image_dir: Path, output: Path) -> str:
    lines = [
        "# DDGP Single-Pruning Experiments",
        "",
        "_Auto-generated by `scripts/ddgp_build_experiments_md.py`. Do not edit manually._",
        "",
        "This report groups selected single-pruning DDGP instances by instance id. Each instance includes the solution counts, dependency-set metrics, predecessor sets, adjacency matrices, the dependency DAG, and the base-clique transition graph.",
        "",
        "In the base-clique transition graph, each node is a distinct predecessor clique $U_i$ used to place one or more vertices. A directed edge $U_i \\to U_j$ means that a vertex placed from $U_i$ is later reused inside $U_j$; dotted edges mark large clique overlap without direct reuse.",
        "",
        "## Selected Instances",
        "",
        "| Instance | Kind | Pruning edge | Base count | Solutions | Local solutions | Fraction | Union choices | Symdiff choices | Intersection choices |",
        "|---|---|---:|---:|---:|---:|---:|---:|---:|---:|",
    ]

    for record in instances:
        row = counts_by_id[record["id"]]
        lines.append(
            "| "
            + " | ".join(
                [
                    f"[{record['id']}](#{record['id']})",
                    row["pruning_kind"],
                    row["pruning_edge"],
                    row["base_count"],
                    row["valid_count"],
                    row["local_valid_count"],
                    row["valid_fraction_of_base"],
                    row["union_choices"],
                    row["symdiff_choices"],
                    row["intersection_choices"],
                ]
            )
            + " |"
        )

    for record in instances:
        row = counts_by_id[record["id"]]
        instance_id = record["id"]
        lines.extend(
            [
                "",
                f"## {instance_id}",
                "",
                "| Field | Value |",
                "|---|---|",
                f"| Kind | `{row['pruning_kind']}` |",
                f"| Pruning edge | `{row['pruning_edge']}` |",
                f"| Raw count | `{row['raw_count']}` |",
                f"| Base count | `{row['base_count']}` |",
                f"| Solutions | `{row['valid_count']}` |",
                f"| Removed by pruning | `{row['removed_by_pruning']}` |",
                f"| Fraction of base | `{row['valid_fraction_of_base']}` |",
                f"| Local raw count | `{row['local_raw_count']}` |",
                f"| Local base count | `{row['local_base_count']}` |",
                f"| Local solutions | `{row['local_valid_count']}` |",
                f"| Local removed by pruning | `{row['local_removed_by_pruning']}` |",
                f"| Local fraction of base | `{row['local_valid_fraction_of_base']}` |",
                f"| Outside choices | `{row['outside_choices']}` |",
                f"| Union choices | `{row['union_choices']}` |",
                f"| Symdiff choices | `{row['symdiff_choices']}` |",
                f"| Intersection choices | `{row['intersection_choices']}` |",
                f"| $\\operatorname{{Fix}}_U(u)$ | `{row['fix_u']}` |",
                f"| $\\operatorname{{Fix}}_U(v)$ | `{row['fix_v']}` |",
                f"| $\\operatorname{{Fix}}_U(u) \\cup \\operatorname{{Fix}}_U(v)$ | `{row['fix_union']}` |",
                f"| $\\operatorname{{Fix}}_U(u) \\cap \\operatorname{{Fix}}_U(v)$ | `{row['fix_intersection']}` |",
                f"| $\\operatorname{{Fix}}_U(u) \\triangle \\operatorname{{Fix}}_U(v)$ | `{row['fix_symdiff']}` |",
                "",
                f"Predecessor sets: {predecessor_sets_text(record)}",
                "",
            ]
        )
        lines.extend(image_table(instance_id, image_dir, output))

    return "\n".join(lines) + "\n"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--instances", type=Path, default=Path("experiments/single-pruning/selected-instances.jsonl"))
    parser.add_argument("--counts", type=Path, default=Path("experiments/single-pruning/selected-counts.csv"))
    parser.add_argument("--image-dir", type=Path, default=Path("experiments/single-pruning/selected-images"))
    parser.add_argument("--out", type=Path, default=Path("experiments.md"))
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    counts_by_id = load_counts(args.counts)
    instances = load_instances(args.instances)
    missing = [record["id"] for record in instances if record["id"] not in counts_by_id]
    if missing:
        raise RuntimeError(f"Missing count rows for: {', '.join(missing)}")

    args.out.write_text(build_report(instances, counts_by_id, args.image_dir, args.out), encoding="utf-8")
    print(f"wrote {args.out}")


if __name__ == "__main__":
    main()

def load_counts(path: Path) -> dict[str, dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as fh:
        return {row["id"]: row for row in csv.DictReader(fh)}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--instances", type=Path, default=Path("experiments/single-pruning/instances.jsonl"))
    parser.add_argument("--counts", type=Path, default=Path("experiments/single-pruning/counts.csv"))
    parser.add_argument("--out", type=Path, default=Path("research/hypothesis-metrics.csv"))
    parser.add_argument("--tol", type=float, default=1e-5)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    counts_by_id = load_counts(args.counts)
    rows = []
    with args.instances.open(encoding="utf-8") as fh:
        for line in fh:
            if not line.strip():
                continue
            instance = load_instance(json.loads(line))
            rows.append(hypothesis_row(instance, counts_by_id[instance.instance_id], args.tol))

    if not rows:
        raise RuntimeError(f"No instances found in {args.instances}.")

    args.out.parent.mkdir(parents=True, exist_ok=True)
    with args.out.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)

    print(f"wrote {len(rows)} row(s) to {args.out}")


if __name__ == "__main__":
    main()
